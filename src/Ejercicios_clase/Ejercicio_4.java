package Ejercicios_clase;

/*Transformar el siguiente bucle for en un bucle while:

for (int i = 0; i < 10; i++) {
System.out.print(i);
}
System.out.println("");

 */

public class Ejercicio_4 {
    public static void main(String[] args) {
    	
    	int i = 0;
    	while (i<10) {
    		System.out.print(i); 
    		System.out.print(" "); 
    		i++;
    	}

    }
}
